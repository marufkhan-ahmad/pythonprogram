# A variable name must start with a letter or an underscore
# A variable name can not start with the number
# A variable name contains alpha numeric characters and underscore.
# for e.g (A-z, 0-9 and _)
# Variable names are case-sensitive(Age,age,and AGE are three different variables)


# Camel-Case:=>
# In camel-case each words, except the first, starts with a capital letter

myVariableName = "Maruf khan"
print(myVariableName)

# Pascal-Case:=>
# In pascal-case each word start with a capital letter

MyVariableName = "Tanveer khan"
print(MyVariableName)

# Snake-case:=>
# In snake-case each word is seperated by an underscore character

My_Variable_Name = "Sahid khan  and Sohaib ali"
print(My_Variable_Name)