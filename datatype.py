# A variable can store data of different types
# string data types

name="Marufkhan"
print(type(name))

# numeric data type

a=3
b=9.0
c=1j
print(type(a))
print(type(b))
print(type(c))

# Sequence data type
# 1. List
fruits = ["Banana", "Grapes", "Mango", "Apple"]
print(type(fruits))

# 2.tuple

fruits = ("Banana", "Grapes", "Mango", "Apple")
print(type(fruits))

# 3. range
x = range(6)
print(type(x))

# Mapping data type
# dictionary
d={"name":"Maruf", "age":"23"}
print(type(d))